int Ocurrencias_Recursivo (
  int _arreglo[],
  int _largo,
  int _valor
){

  //----------------------------------------------------------//

  _largo -= 1;

  //----------------------------------------------------------//

  if ( _largo == -1 )
      return 0;

  //----------------------------------------------------------//

  bool ocurrencia = ( _arreglo[ _largo ] == _valor );

  //----------------------------------------------------------//

  return
    Ocurrencias_Recursivo(
      _arreglo,
      _largo,
      _valor
    ) + ocurrencia;

  //----------------------------------------------------------//

}

