int Mininimo_Iterativo (
  int _arreglo [],
  int _largo
){

  //----------------------------------------------------------//

  int minimo = _arreglo[ 0 ];

  //----------------------------------------------------------//

  for (  int i=1; i<_largo;  i+=1  )
    if ( minimo > _arreglo[ i ] )
      minimo = _arreglo[ i ];

  //----------------------------------------------------------//

  return minimo;

  //----------------------------------------------------------//
}

