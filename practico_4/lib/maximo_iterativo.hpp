int Maximo_Iterativo (
  int _arreglo [],
  int _largo
){

  //----------------------------------------------------------//

  int maximo = _arreglo[ 0 ];

  //----------------------------------------------------------//

  for (  int i=1; i<_largo;  i+=1  )
    if ( maximo < _arreglo[ i ] )
      maximo = _arreglo[ i ];

  //----------------------------------------------------------//

  return maximo;

  //----------------------------------------------------------//

}

