void Invierte_Iterativo (
  int _arreglo [],
  int _largo
){

  //----------------------------------------------------------//

  largo /= 2;

  //----------------------------------------------------------//

  for (  int i=0;  i<largo;  i+=1  )
    std::swap(
      (arreglo [i]),
      (arreglo[largo-1-i])
    );

  //----------------------------------------------------------//

}

