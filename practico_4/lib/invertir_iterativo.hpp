void Invierte_Iterativo (
  int _arreglo [],
  int _largo
){

  //----------------------------------------------------------//

  _largo /= 2;

  //----------------------------------------------------------//

  for (  int i=0;  i<_largo;  i+=1  )
    Intercambiar(
      &(_arreglo [i]),
      &(_arreglo[_largo-1-i])
    );

  //----------------------------------------------------------//

}

